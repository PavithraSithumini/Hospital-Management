    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        int patientID=Integer.valueOf(txtPatientID.getText());
        String firstName=txtFirstName.getText();
        String lastName=txtLastName.getText();
        String gender=" ";
        if(rdoMale.isSelected()){
            gender="Male";
        }else if(rdoFemale.isSelected()){
            gender="Female";
        }
        Date dob=Date.valueOf(txtDOB.getText());
        String telephone=txtTelephone.getText();
        String category=" ";
        if(rdoInPatient.isSelected()){
            category="InPatient";
        }else if(rdoOutPatient.isSelected()){
            category="OutPatient";
        }

        Patient p=new Patient(patientID, firstName, lastName, gender, dob, telephone, category);
        boolean flag=pDB.add(p);
        if(flag){
            JOptionPane.showMessageDialog(pnlPatient, "Patient Added");
        }else{
            JOptionPane.showMessageDialog(pnlPatient, "Patient not Added");
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnAllActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAllActionPerformed
        // TODO add your handling code here:
        tblModel.setRowCount(0);
        ArrayList<Patient> pList=pDB.getAll();
        for(Patient p: pList){
           int patientID=p.getPatientID();
           String firstName=p.getFirstName();
           String lastName=p.getLastName();
           String gender=p.getGender();
           Date dob=p.getDob();
           String telephone=p.getTel();
           String category=p.getCategory();
           
           tblModel.addRow(new Object[]{patientID,firstName,lastName,gender,dob,telephone, category});
        }
    }//GEN-LAST:event_btnAllActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int patientId=Integer.valueOf(JOptionPane.showInputDialog("Enter the Patient ID"));
        boolean flag=pDB.delete(patientId);
        if(flag){
            JOptionPane.showMessageDialog(pnlPatient, "Patient Record is Deleted");
        }else{
            JOptionPane.showMessageDialog(pnlPatient, "Patient Record is not Deleted");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        // TODO add your handling code here:
        int patientId=Integer.valueOf(JOptionPane.showInputDialog("Enter the Patient ID"));
        Patient p=pDB.get(patientId);
        if(p!=null){
            txtPatientID.setText(String.valueOf(p.getPatientID()));
            txtFirstName.setText(p.getFirstName());
            txtLastName.setText(p.getLastName());
            if(p.getGender().equals("Male")){
                rdoMale.setSelected(true);
            }else if(p.getGender().equals("Female")){
                rdoFemale.setSelected(true);
            }
            txtDOB.setText(String.valueOf(p.getDob()));
            txtTelephone.setText(p.getTel());
            if(p.getCategory().equals("InPatient")){
                rdoInPatient.setSelected(true);
            }else if(p.getCategory().equals("OutPatient")){
                rdoOutPatient.setSelected(true);
            }
        }else{
            JOptionPane.showMessageDialog(pnlPatient, "The patient record is not available");
        }
    }//GEN-LAST:event_btnFindActionPerformed

