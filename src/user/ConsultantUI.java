    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int cID;
    try {
        cID = Integer.parseInt(JOptionPane.showInputDialog("Enter Consultant ID to delete:"));
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Consultant ID must be a number.", "Input Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    // Delete the consultant from the database
    boolean success = cDB.delete(cID);

    // Show a message based on the result
    if (success) {
        JOptionPane.showMessageDialog(this, "Consultant deleted successfully!");
    } else {
        JOptionPane.showMessageDialog(this, "Failed to delete consultant.");
    }


    }//GEN-LAST:event_btnDeleteActionPerformed

