    private void btnAllActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAllActionPerformed
        // TODO add your handling code here:
        tblModel.setRowCount(0);

    // Retrieve all consultants from the database
    ArrayList<Consultant> cList = cDB.getAll();

    // If consultants are found, add them to the table
    if (cList != null) {
        for (Consultant c : cList) {
            tblModel.addRow(new Object[]{
                c.getcID(),
                c.getFirstName(),
                c.getLastName(),
                c.getCategory(),
                c.getHospital(),
                c.getTelephone(),
            });
        }
    } else {
        JOptionPane.showMessageDialog(this, "No consultants found.");
    }
    }//GEN-LAST:event_btnAllActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        int consultantID;
    try {
        consultantID = Integer.parseInt(txtCunsultantID.getText());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Consultant ID must be a number.", "Input Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
        String firstName=txtFirstName.getText();
        String lastName=txtLastName.getText();
        String category = cmbCategory.getSelectedItem().toString();
        String hospital=txtHospital.getText();
        String telephone=txtTelephone.getText();

        Consultant c=new Consultant(consultantID, firstName, lastName, category, hospital, telephone);
        boolean flag=cDB.add(c);
        if(flag){
            JOptionPane.showMessageDialog(pnlConsultant, "Consultant Added");
        }else{
            JOptionPane.showMessageDialog(pnlConsultant, "Consultant not Added");
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        // TODO add your handling code here:
        // Validate Consultant ID
        int consultantID;
        try {
            consultantID = Integer.parseInt(JOptionPane.showInputDialog("Enter the Consultant ID"));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Consultant ID must be a number.", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        Consultant c = cDB.get(consultantID);
        if(c!=null){
            txtCunsultantID.setText(String.valueOf(c.getcID()));
            txtFirstName.setText(c.getFirstName());
            txtLastName.setText(c.getLastName());
            cmbCategory.setSelectedItem(c.getCategory());
            txtTelephone.setText(c.getTelephone());
        }else{
            JOptionPane.showMessageDialog(pnlConsultant, "The patient record is not available");
        }
    }//GEN-LAST:event_btnFindActionPerformed

